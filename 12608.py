alp_to_num = {
    "a": ("2", 2),
    "b": ("22", 2),
    "c": ("222", 2),
    "d": ("3", 3),
    "e": ("33", 3),
    "f": ("333", 3),
    "g": ("4", 4),
    "h": ("44", 4),
    "i": ("444", 4),
    "j": ("5", 5),
    "k": ("55", 5),
    "l": ("555", 5),
    "m": ("6", 6),
    "n": ("66", 6),
    "o": ("666", 6),
    "p": ("7", 7),
    "q": ("77", 7),
    "r": ("777", 7),
    "s": ("7777", 7),
    "t": ("8", 8),
    "u": ("88", 8),
    "v": ("888", 8),
    "w": ("9", 9),
    "x": ("99", 9),
    "y": ("999", 9),
    "z": ("9999", 9),
    " ": ("0", 0),
}

n = int(input())

for i in range(n):
    prev_state = -1
    s = input()

    rst = ""
    for c in s:
        state = alp_to_num[c][1]

        if prev_state != state:
            rst += alp_to_num[c][0]
        else:
            rst += " "
            rst += alp_to_num[c][0]

        prev_state = state

    print(f"Case #{i+1}: {rst}")
